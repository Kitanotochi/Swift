// Инструкция "if" в Swift

let a = 3 // Создали константу "a"
let b = 1 // Создали константу "b"
// Условие
if a == b { 
   print("a equals b") // Если "a" равно "b" вывести результат
}
else if a < b { // Или если "a" больше "b" вывести результат
    print("b exceeds a by \(b - a)") // На сколько превышает
}
